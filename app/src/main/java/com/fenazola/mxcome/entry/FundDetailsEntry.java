public class FundDetailsEntry {
    private String title;
    private String mny;
    private String date;
    private String sumMny;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getMny() {
        return mny;
    }

    public void setMny(String mny) {
        this.mny = mny;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getSumMny() {
        return sumMny;
    }

    public void setSumMny(String sumMny) {
        this.sumMny = sumMny;
    }
}
